class Calculadora {
    sumar(num1, num2) {
        return num1 + num2;
    }

    restar(num1, num2) {
        return num1 - num2;
    }

    dividir(num1, num2) {
        return num1 / num2;
    }

    multiplicar(num1, num2) {
        return num1 * num2;
    }
    raizCuadrada(n1) {
        return Math.sqrt(n1);
    }
    potencia(n1, n2) {
        return Math.pow(n1, n2);
    }
}
